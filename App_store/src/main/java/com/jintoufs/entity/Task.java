package com.jintoufs.entity;

import java.io.Serializable;

public class Task implements Serializable{
	
	private long id;
	private String passwordStation="";
	private String keyStation="";
	private Integer keyId=0;
	private Integer passwordId=0;
	private String executeTime="";
	private int cashboxAmount;
	private int status;
	
	private String routeName="";
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	
	public String getPasswordStation() {
		return passwordStation;
	}
	public void setPasswordStation(String passwordStation) {
		this.passwordStation = passwordStation;
	}
	public String getKeyStation() {
		return keyStation;
	}
	public void setKeyStation(String keyStation) {
		this.keyStation = keyStation;
	}
	public Integer getKeyId() {
		return keyId;
	}
	public void setKeyId(Integer keyId) {
		this.keyId = keyId;
	}
	public Integer getPasswordId() {
		return passwordId;
	}
	public void setPasswordId(Integer passwordId) {
		this.passwordId = passwordId;
	}
	public String getExecuteTime() {
		return executeTime;
	}
	public void setExecuteTime(String executeTime) {
		this.executeTime = executeTime;
	}
	public int getCashboxAmount() {
		return cashboxAmount;
	}
	public void setCashboxAmount(int cashboxAmount) {
		this.cashboxAmount = cashboxAmount;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getRouteName() {
		return routeName;
	}
	public void setRouteName(String routeName) {
		this.routeName = routeName;
	}
	
}