package com.jintoufs.logstics.entity;

public class Terminal extends Entity {
	private String name="";
	private String code="";
	private String address="";
	private String verifyCode="";
	private String idCode="";
	private String rfid;
	private long detailId;
	private int lockType;//0-机械锁；1-电子锁
	private String siteName;//网点名字
	private String siteShort;//网点名字缩写
	private int status;//状态  0-未完成  1-尾箱已送，2-已完成（尾箱已收取）
	private int state;// 0-出库 1-入库
	private String type;// 1:残钞，2：上门收款，3：尾箱寄存，4：网点配钞，5：网点上缴，6：加钞，7：贵金属业务，8：维保
	private Double longitude;// 经度
	private Double latitude;// 纬度
	private int deliverType;// 收送箱类型；0送，1收

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public Double getLongitude() {
		return longitude;
	}

	public void setLongitude(Double longitude) {
		this.longitude = longitude;
	}

	public Double getLatitude() {
		return latitude;
	}

	public void setLatitude(Double latitude) {
		this.latitude = latitude;
	}

	public long getDetailId() {
		return detailId;
	}

	public void setDetailId(long detailId) {
		this.detailId = detailId;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getVerifyCode() {
		return verifyCode;
	}

	public void setVerifyCode(String verifyCode) {
		this.verifyCode = verifyCode;
	}

	public int getDeliverType() {
		return deliverType;
	}

	public void setDeliverType(int deliverType) {
		this.deliverType = deliverType;
	}

	public String getSiteName() {
		return siteName;
	}

	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}

	public String getSiteShort() {
		return siteShort;
	}

	public void setSiteShort(String siteShort) {
		this.siteShort = siteShort;
	}

	public String getIdCode() {
		return idCode;
	}

	public void setIdCode(String idCode) {
		this.idCode = idCode;
	}

	public int getLockType() {
		return lockType;
	}

	public void setLockType(int lockType) {
		this.lockType = lockType;
	}

	public String getRfid() {
		return rfid;
	}

	public void setRfid(String rfid) {
		this.rfid = rfid;
	}

}